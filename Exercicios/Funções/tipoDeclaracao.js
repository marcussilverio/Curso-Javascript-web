//function declaration
function soma(x,y){
    return x+y
}

//function expression
const sub = function(x,y){
    return x-y
}
//named function expression
const mult = function mult(x,y){
    return x*y
}
/*
    function declaration, posso chamar em qualquer lugar do codigo independente
    dela ja estar declarada ou nao.
    tanto a expression quanto a named expression precisam estar declarada acima da
    chamada para que funcione
*/